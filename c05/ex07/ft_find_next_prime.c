int	ft_is_prime(int nb)
{
	int i;

	if (nb < 2)
		return (1);
	if ((nb % 2) == 0)
		return (0);
	if ((nb % 3) == 0)
		return (0);
	i = 2;
	while (i <= (nb / i))
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (!ft_is_prime(nb))
		nb++;
	if (nb <= 2)
		return (2);
	return (nb);
}
