int	ft_sqrt(int nb)
{
	int temp;
	int i;

	temp = 0;
	i = 0;
	while (i <= (nb / i))
	{
		i++;
		temp = i * i;
		if (nb == temp)
			return (i);
	}
	return (0);
}
